// == vs ===

let var1="7";
let var2=7;

console.log(var1==var2); //This will not check the datatype of the variable will only check the value
console.log(var1 === var2) //This will check the datatype of the variable with the value

// != and !==

let a = 7
let b = "7";

console.log(a != b); //This will not check the datatype of the variable will only check the value
console.log(a !== b); //This will check the datatype of the variable with the value